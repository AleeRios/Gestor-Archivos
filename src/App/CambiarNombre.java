    public void cambiarNom(){
        String ruta=txtRuta.getText();
        File f1=new File(ruta);
        String s=f1.getParent()+"/"+txtNom.getText();
        if(f1.exists()){
            f1.renameTo(new File(s));
            JOptionPane.showMessageDialog(null,"Archivo renombrado");
        }else JOptionPane.showMessageDialog(null,"Error al renombrar");
            
    }

