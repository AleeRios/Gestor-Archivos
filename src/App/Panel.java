    public void opciones(){
         String op=combo.getSelectedItem().toString();
         
         switch(op){
             case "Crear":
                 this.dispose();
                 CrearA cre=new CrearA();
                 break;
             case "Mover":
                 this.dispose();
                 MoverA mov=new MoverA();
                 break;
             case "Copiar":
                 this.dispose();
                 CopiarA cop=new CopiarA();
                 break;
             case "Borrar":
                 this.dispose();
                 BorraA bor=new BorraA();
                 break;
             case "Cambiar nombre":
                 this.dispose();
                 CambiarNombre cam=new CambiarNombre();
                 break;
             default:
                 JOptionPane.showMessageDialog(null,"Opcion no valida");
                 break;
         }
    }
    
